// URLs que NO deben cachearse
const NO_CACHE_URLS = [
    '/api/config', // Endpoint din√°mico
    'chrome-extension://',
    'https://cdn.onesignal.com/', // OneSignal maneja su propio cache
    'onesignal.com', // Evitar conflictos con OneSignal
    'OneSignalSDK'
];

// Instalaci√≥n del Service Worker
self.addEventListener('install', event => {
    console.log('üîß Service Worker: Instalando versi√≥n', CACHE_NAME);

    event.waitUntil(
        caches.open(STATIC_CACHE)
            .then(cache => {
                console.log('üì¶ Service Worker: Cacheando archivos est√°ticos...');
                return cache.addAll(STATIC_ASSETS.filter(url => !url.startsWith('http')));
            })
            .then(() => {
                console.log('‚úÖ Service Worker: Instalaci√≥n completada');
                // Solo skipWaiting si es la primera instalaci√≥n
                if (!self.registration.active) {
                    console.log('üöÄ Service Worker: Primera instalaci√≥n, activando inmediatamente');
                    return self.skipWaiting();
                } else {
                    console.log('‚è≥ Service Worker: Esperando activaci√≥n manual para actualizaci√≥n');
                }
            })
            .catch(error => {
                console.error('‚ùå Service Worker: Error en instalaci√≥n:', error);
            })
    );
});

// Activaci√≥n del Service Worker
self.addEventListener('activate', event => {
    console.log('üöÄ Service Worker: Activando...');

    event.waitUntil(
        caches.keys()
            .then(cacheNames => {
                return Promise.all(
                    cacheNames.map(cacheName => {
                        // Eliminar caches antiguos
                        if (cacheName !== STATIC_CACHE && cacheName !== DYNAMIC_CACHE) {
                            console.log('üóëÔ∏è Service Worker: Eliminando cache antiguo:', cacheName);
                            return caches.delete(cacheName);
                        }
                    })
                );
            })
            .then(() => {
                console.log('‚úÖ Service Worker: Activaci√≥n completada');
                // Tomar control inmediato de todas las pesta√±as
                return self.clients.claim();
            })
    );
});

// Intercepci√≥n de peticiones (estrategia Cache First para est√°ticos, Network First para din√°micos)
self.addEventListener('fetch', event => {
    const url = event.request.url;

    // No cachear URLs espec√≠ficas o OneSignal
    if (NO_CACHE_URLS.some(pattern => url.includes(pattern))) {
        return;
    }

    // Evitar interferir con OneSignal SDK
    if (url.includes('OneSignal') || url.includes('onesignal')) {
        return;
    }

    // Solo manejar peticiones GET
    if (event.request.method !== 'GET') {
        return;
    }

    event.respondWith(
        handleFetchRequest(event.request)
    );
});

async function handleFetchRequest(request) {
    const url = request.url;

    try {
        // Estrategia para archivos est√°ticos (Cache First)
        if (STATIC_ASSETS.some(asset => url.includes(asset))) {
            return await cacheFirst(request);
        }

        // Estrategia para API y contenido din√°mico (Network First)
        if (url.includes('/api/') || url.includes('cdn.onesignal.com')) {
            return await networkFirst(request);
        }

        // Por defecto: Cache First con fallback a Network
        return await cacheFirst(request);

    } catch (error) {
        console.warn('‚ö†Ô∏è Service Worker: Error en fetch:', error);

        // Fallback offline para navegaci√≥n
        if (request.destination === 'document') {
            return caches.match('/index.html');
        }

        // Para otros recursos, devolver respuesta de error
        return new Response('Recurso no disponible offline', {
            status: 503,
            statusText: 'Service Unavailable'
        });
    }
}

// Estrategia Cache First
async function cacheFirst(request) {
    const cachedResponse = await caches.match(request);

    if (cachedResponse) {
        // Actualizar en background
        fetch(request)
            .then(response => updateCache(request, response.clone()))
            .catch(() => { }); // Ignorar errores de red

        return cachedResponse;
    }

    // Si no est√° en cache, obtener de la red y cachear
    const networkResponse = await fetch(request);
    await updateCache(request, networkResponse.clone());
    return networkResponse;
}

// Estrategia Network First
async function networkFirst(request) {
    try {
        const networkResponse = await fetch(request);
        await updateCache(request, networkResponse.clone());
        return networkResponse;
    } catch (error) {
        // Si falla la red, usar cache
        const cachedResponse = await caches.match(request);
        if (cachedResponse) {
            return cachedResponse;
        }
        throw error;
    }
}

// Actualizar cache din√°micamente
async function updateCache(request, response) {
    // Solo cachear respuestas exitosas
    if (response.status === 200) {
        const cache = await caches.open(DYNAMIC_CACHE);
        await cache.put(request, response);
    }
}

// Mensaje de comunicaci√≥n con la aplicaci√≥n principal
self.addEventListener('message', event => {
    console.log('üì® Service Worker: Mensaje recibido:', event.data);

    if (event.data && event.data.type === 'SKIP_WAITING') {
        console.log('‚è≠Ô∏è Service Worker: Ejecutando skipWaiting()...');
        self.skipWaiting();
    }

    if (event.data && event.data.type === 'GET_VERSION') {
        event.ports[0].postMessage({ version: CACHE_NAME });
    }

    // Manejar solicitudes de notificaci√≥n desde la p√°gina principal
    if (event.data && event.data.type === 'SHOW_NOTIFICATION') {
        console.log('üîî Service Worker: Mostrando notificaci√≥n solicitada');
        const { title, options } = event.data;
        showNotificationCorrectly(title, options);
    }
});

// Notificaciones push (compatible con OneSignal)
self.addEventListener('push', event => {
    console.log('üìß Service Worker: Push recibido');

    // Verificar si OneSignal est√° manejando esto
    if (event.data && event.data.text().includes('onesignal')) {
        console.log('üîî OneSignal manejando push notification');
        return; // Dejar que OneSignal lo maneje
    }

    // Manejar notificaciones propias si es necesario
    // OneSignal maneja las notificaciones push autom√°ticamente
});

// Interceptar errores de construcci√≥n de notificaciones
self.addEventListener('error', event => {
    console.log('‚ö†Ô∏è Service Worker: Error interceptado:', event.error);
    
    if (event.error && event.error.message && event.error.message.includes('Illegal constructor')) {
        console.log('üîß Interceptando error de construcci√≥n de notificaci√≥n');
        event.preventDefault();
        return false;
    }
});

// Funci√≥n para mostrar notificaciones usando el m√©todo correcto del SW
async function showNotificationCorrectly(title, options = {}) {
    try {
        if (self.registration) {
            // Configuraciones optimizadas para m√≥viles
            const mobileOptimizedOptions = {
                body: options.body || 'Notificaci√≥n de CaminoMedio Alertas',
                icon: options.icon || '/assets/icons/192x192.png',
                badge: options.badge || '/assets/icons/48x48.png',
                image: options.image || '/assets/icons/512x512.png', // Imagen grande para m√≥viles
                requireInteraction: true, // IMPORTANTE: Mantener visible hasta que el usuario interact√∫e
                persistent: true, // Hacer la notificaci√≥n persistente
                silent: false, // Permitir sonido
                vibrate: [200, 100, 200, 100, 200], // Vibraci√≥n m√°s larga para m√≥viles
                actions: [
                    {
                        action: 'view',
                        title: 'üëÄ Ver',
                        icon: '/assets/icons/24x24.png'
                    },
                    {
                        action: 'close',
                        title: '‚ùå Cerrar',
                        icon: '/assets/icons/24x24.png'
                    }
                ],
                data: {
                    timestamp: Date.now(),
                    source: 'caminomedio-alertas',
                    mobile_optimized: true
                },
                tag: options.tag || 'caminomedio-alert', // Evitar duplicados
                renotify: true, // Renotificar incluso si hay una similar
                ...options // Mantener opciones personalizadas
            };
            
            console.log('üì± Mostrando notificaci√≥n optimizada para m√≥vil:', title);
            return await self.registration.showNotification(title, mobileOptimizedOptions);
        } else {
            console.warn('‚ö†Ô∏è Service Worker registration no disponible');
        }
    } catch (error) {
        console.error('‚ùå Error mostrando notificaci√≥n:', error);
    }
}

// Click en notificaciones
self.addEventListener('notificationclick', event => {
    console.log('üîî Service Worker: Click en notificaci√≥n');
    
    // Obtener la acci√≥n clickeada
    const action = event.action;
    const notification = event.notification;
    
    console.log('üéØ Acci√≥n de notificaci√≥n:', action);
    
    // Cerrar la notificaci√≥n
    notification.close();
    
    // Manejar diferentes acciones
    if (action === 'close') {
        console.log('‚ùå Usuario cerr√≥ la notificaci√≥n');
        return;
    }
    
    // Para 'view' o click directo, abrir/enfocar la aplicaci√≥n
    event.waitUntil(
        clients.matchAll({ 
            type: 'window',
            includeUncontrolled: true // Incluir ventanas no controladas
        })
            .then(clientList => {
                console.log('üîç Buscando ventanas abiertas:', clientList.length);
                
                // Si hay una ventana abierta, enfocarla
                for (let client of clientList) {
                    if (client.url.includes(location.origin) && 'focus' in client) {
                        console.log('üéØ Enfocando ventana existente');
                        return client.focus();
                    }
                }
                
                // Si no hay ventana abierta, abrir una nueva
                if (clients.openWindow) {
                    console.log('üÜï Abriendo nueva ventana');
                    return clients.openWindow('/');
                }
            })
            .catch(error => {
                console.error('‚ùå Error manejando click de notificaci√≥n:', error);
            })
    );
});

